print("Este porgrama convierte el número a su equivalente en letras")

num = int(input("¿Cuál número desea convertir?: "))

if num == 1:
    print("El número es Uno")
elif num == 2:
    print("El número es Dos")
elif num == 3:
    print("El número es Tres")
elif num == 4:
    print("El número es Cuatro")
elif num == 5:
    print("El número es Cinco")
else:
    print("Este programa sólo permite números de 1 a 5")
